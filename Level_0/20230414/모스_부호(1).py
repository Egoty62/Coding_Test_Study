# letter의 모스부호는 공백으로 구분 되어있는 문자열 => split() 사용
# 주어진 딕셔너리를 사용하여 변환 후 join() 사용

def solution(letter):
    morse = { 
    '.-':'a','-...':'b','-.-.':'c','-..':'d','.':'e','..-.':'f',
    '--.':'g','....':'h','..':'i','.---':'j','-.-':'k','.-..':'l',
    '--':'m','-.':'n','---':'o','.--.':'p','--.-':'q','.-.':'r',
    '...':'s','-':'t','..-':'u','...-':'v','.--':'w','-..-':'x',
    '-.--':'y','--..':'z'
}
    answer_list = letter.split()
    
    return ''.join([morse[i] for i in answer_list])